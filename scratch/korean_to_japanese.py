# Sort by length descending so longer matches are tried first
REPLACEMENTS.sort(key=lambda x: len(x[0]), reverse=True)

def has_korean(text):
    return bool(KOREAN_RE.search(text))

def replace_korean(text):
    for korean, japanese in REPLACEMENTS:
        text = text.replace(korean, japanese)
    return text

def process_file(filepath):
    """Process a single file, replacing Korean with Japanese."""
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
    except (UnicodeDecodeError, PermissionError):
        return 0
    
    if not has_korean(content):
        return 0
    
    new_content = replace_korean(content)
    
    if new_content != content:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(new_content)
        
        # Count remaining Korean
        remaining = KOREAN_RE.findall(new_content)
        return len(remaining)
    return 0

def main():
    root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    skip_dirs = {'.git', '__pycache__', 'node_modules', '.venv', 'venv', 'scratch', '.gemini'}
    extensions = {'.py', '.html', '.js', '.css', '.json', '.md', '.yaml', '.yml', '.toml'}
    
    total_files = 0
    modified_files = 0
    files_with_remaining = []
    
    for dirpath, dirnames, filenames in os.walk(root):
        dirnames[:] = [d for d in dirnames if d not in skip_dirs]
        for fname in filenames:
            ext = os.path.splitext(fname)[1].lower()
            if ext not in extensions:
                continue
            
            fpath = os.path.join(dirpath, fname)
            total_files += 1
            
            try:
                with open(fpath, 'r', encoding='utf-8') as f:
                    content = f.read()
                if not has_korean(content):
                    continue
            except:
                continue
            
            remaining = process_file(fpath)
            modified_files += 1
            rel_path = os.path.relpath(fpath, root)
            
            if remaining > 0:
                files_with_remaining.append((rel_path, remaining))
                print(f"  修正: {rel_path} (残り韓国語文字: {remaining})")
            else:
                print(f"  完了: {rel_path}")
    
    print(f"\n=== 結果 ===")
    print(f"スキャンファイル数: {total_files}")
    print(f"修正ファイル数: {modified_files}")
    
    if files_with_remaining:
        print(f"\n⚠ まだ韓国語が残っているファイル ({len(files_with_remaining)}件):")
        for fp, count in files_with_remaining:
            print(f"  - {fp}: {count}文字")

if __name__ == "__main__":
    main()
