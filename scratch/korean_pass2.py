# Sort by length descending
EXTRA.sort(key=lambda x: len(x[0]), reverse=True)

def process_file(filepath):
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
    except:
        return False
    
    if not KOREAN_RE.search(content):
        return False
    
    original = content
    for korean, japanese in EXTRA:
        content = content.replace(korean, japanese)
    
    if content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        return True
    return False

root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
skip_dirs = {'.git', '__pycache__', 'node_modules', '.venv', 'venv', 'scratch', '.gemini'}
extensions = {'.py', '.html', '.js', '.css', '.md', '.yaml'}

modified = 0
for dirpath, dirnames, filenames in os.walk(root):
    dirnames[:] = [d for d in dirnames if d not in skip_dirs]
    for fname in filenames:
        ext = os.path.splitext(fname)[1].lower()
        if ext not in extensions: continue
        fpath = os.path.join(dirpath, fname)
        if process_file(fpath):
            modified += 1
            rel = os.path.relpath(fpath, root)
            # Check remaining
            with open(fpath, 'r', encoding='utf-8') as f:
                remaining = len(KOREAN_RE.findall(f.read()))
            if remaining > 0:
                print(f"  {rel}: {remaining} chars remaining")
            else:
                print(f"  {rel}: CLEAN")

print(f"\nModified: {modified} files")
